// variables and operators

/*
    There are three ways to declare a variable
    1. var --> function scoped, can be redeclared ( var is not recommended)
        --> since it can be redeclared , it causes problem and hence it is advisable not to use in prorgramming in js
        --> since it can be redeclared, it happens we can make mistake of declaring same named variable again and again

    2. let --> Block scoped, can be re-assigned
        --> notice here , we can re-assign the the variable using the let syntax
        --> it is used if the variable holds the value that needs to be changed over and over again
    
    3. const --> Block scoped, cannot be re-assigned
        --> notice that we can not reassign the variable if it is declared using const


        
 */

// Data Types in javascript

/* 
    **Primitive Data Types: **
        - `String` : Text values ("Hello")
        - `Number` : Numeric values (25,3.14)
        - `Boolean` : True/false (true, false)
        - `Undefined` : A variable declared but not assigned
        - `Null` : Represents "nothing" (let y = null;)
        - `BigInt`: large numbers (BigInt(12345678912345679))
        - `Symbol` : Unique identifiers (Symbol("id"))

    **Non Primitive Data Types: **
        - `Object` : Collection of key-value pairs
        - `Arrary` : Ordered lis tof values
        - `Function` : Code that can be executed
*/
